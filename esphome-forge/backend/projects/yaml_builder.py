"""
YAML-Builder – Generiert vollständiges ESPHome-YAML aus Projekt + Komponenten.

Strategie:
  1. Skeleton: esphome, board, wifi, api, ota, logger (immer da)
  2. Bus-Blöcke: i2c, spi, uart werden auto-injected wenn nötig
  3. Komponenten-Blöcke: sensor, binary_sensor, display, light, switch, etc.
  4. Custom-Components: external_components + raw YAML snippets
"""
